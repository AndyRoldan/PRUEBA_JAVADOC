    void Agregar() {

        String nombre = jTextField_NOMBRE.getText().toUpperCase();

        try {
            if (nombre.equals("")) {
                JOptionPane.showMessageDialog(null, "Faltan ingresar datos");
                limpiarTabla();
            } else {
                String sql = "insert into equipo(nombre_equipo) values('" + nombre + "')";
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Datos ingresados");
                limpiarTabla();
                limpiarcomboX();

            }
        } catch (Exception e) {
        }
    }

    void limpiarTabla() {
        for (int i = 0; i <= jTable_EQUIPO.getRowCount(); i++) {
            modeloEquipo.removeRow(i);
            i = i - 1;
        }
    }

    void limpiarcomboX() {
        for (int i = 0; i <= jComboBox1.getItemCount(); i++) {
            i = i - 1;
        }
    }

    void Eliminar() {
        int fila = jTable_EQUIPO.getSelectedRow();
        try {
            if (fila < 0) {
                JOptionPane.showMessageDialog(null, "Registro no seleccionado");
                limpiarTabla();
            } else {
                String sql = "delete from equipo where id_equipo=" + idc;
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
                limpiarTabla();
            }
        } catch (Exception e) {
        }
    }

    void Modificar() {
        String nombre = jTextField_NOMBRE.getText().toUpperCase();

        try {
            if (nombre.equals("")) {
                JOptionPane.showMessageDialog(null, "Faltan ingresar datos");
                limpiarTabla();
            } else {

                String sql = "Update equipo set nombre_equipo='" + nombre + "' where id_equipo=" + idc;
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Datos Actualizados");
                limpiarTabla();

            }
        } catch (Exception e) {
        }
    }
