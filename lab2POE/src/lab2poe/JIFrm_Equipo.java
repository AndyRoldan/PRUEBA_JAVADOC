    void consultarEquipo() {
        String sql = "SELECT * FROM equipo";
        try {
            conet = con1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] equipo = new Object[2];
            modeloEquipo = (DefaultTableModel) jTable_EQUIPO.getModel();
            while (rs.next()) {
                equipo[0] = rs.getInt("id_equipo");
                equipo[1] = rs.getString("nombre_equipo");

                modeloEquipo.addRow(equipo);
            }
            jTable_EQUIPO.setModel(modeloEquipo);
        } catch (Exception e) {
        }
    }

