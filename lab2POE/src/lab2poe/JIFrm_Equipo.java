    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:

        if (evt.getStateChange() == ItemEvent.SELECTED) {
            Equipo equ = (Equipo) jComboBox1.getSelectedItem();
            try {
                DefaultTableModel modelo = new DefaultTableModel();
                jTable_JE.setModel(modelo);

                PreparedStatement ps = null;
                ResultSet rs = null;
                conexion conn = new conexion();
                java.sql.Connection con = conn.getConnection();

                String sql = "SELECT dui, nombre_completo FROM jugador WHERE id_equipo=" + equ.getId_equipo();
        
                ps = con.prepareStatement(sql);
                rs = ps.executeQuery();

                ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
                int cantidadColumnas = rsMd.getColumnCount();

                modelo.addColumn("DUI JUGADOR");
                modelo.addColumn("NOMBRE JUGADOR");
//                modelo.addColumn("POSICION");
//                 modelo.addColumn("UNIVERSIDAD");
                int[] anchos = {50, 200};
                for (int i = 0; i < jTable_JE.getColumnCount(); i++) {
                    jTable_JE.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
                }

                while (rs.next()) {
                    Object[] filas = new Object[cantidadColumnas];
                    for (int i = 0; i < cantidadColumnas; i++) {
                        filas[i] = rs.getObject(i + 1);
                    }
                    modelo.addRow(filas);
                }
            } catch (SQLException ex) {
                System.err.println(ex.toString());
            }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

